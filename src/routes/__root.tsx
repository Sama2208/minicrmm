const TITLES: Record<string, string> = {
  "/lidlar": "Umumiy lidlar",
  "/hisobotlar": "Hisobotlar",
  "/sozlamalar": "Sozlamalar",
  "/foydalanuvchilar": "Foydalanuvchilar",
  "/mening-lidlarim": "Mening lidlarim",
};

function Shell() {
  const pathname = useRouterState({ select: (r) => r.location.pathname });
  const title = TITLES[pathname] ?? "CRM";

  if (pathname === "/auth" || pathname === "/ariza" || pathname.startsWith("/admin")) {
    return <Outlet />;
  }

  return (
    <SidebarProvider>
      <div className="min-h-screen flex w-full bg-background">
        <AppSidebar />
        <div className="flex-1 flex flex-col min-w-0">
          <header className="h-14 flex items-center gap-3 border-b bg-background px-4 sticky top-0 z-10">
            <SidebarTrigger />
            <h1 className="text-lg font-semibold">{title}</h1>
          </header>
          <main className="flex-1 p-4 md:p-6 min-w-0">
            <Outlet />
          </main>
        </div>
      </div>
    </SidebarProvider>
  );
}

function RootComponent() {
  const { queryClient } = Route.useRouteContext();
  return (
    <QueryClientProvider client={queryClient}>
      <Shell />
      <Toaster />
    </QueryClientProvider>
  );
}
